		'Verify the existance of Login Message '

		var = WebUI.verifyElementPresent(findTestObject('Object Repository/Patient Accounting/Page_DRIS Home Page/span_Login Message'), 10, FailureHandling.CONTINUE_ON_FAILURE)
		if (var){
			WebUI.verifyElementPresent(findTestObject('Object Repository/Patient Accounting/Page_DRIS Home Page/span_Login Message'), 10, FailureHandling.CONTINUE_ON_FAILURE)

			'Click on Do not show on next login checkbox'
			WebUI.click(findTestObject('Object Repository/Patient Accounting/Page_Patient Accounting Search/input_Do not show on next login_clearLoginMessage'))


			'click on Continue button '
			WebUI.click(findTestObject('Object Repository/Patient Accounting/Page_Patient Accounting Search/input_Do not show on next login_btnContinue'))
		}
	}


